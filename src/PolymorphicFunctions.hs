module PolymorphicFunctions where

{-  
  type variables are lowercase and can be nearly anything e.g. a, b, c, x, foo, hello_123 
  concrete types are Uppercase and can be nearly anything e.g. Int, Integer, Double    - note Integer is an arbitrary sized Int
  not the same as OOP polymorphism
  if you are trying to annotate your functions sometimes it's easier and more beneficial
  to let the compiler try to tell you the type so you can correct it or just copy and use it as an annotation.
-}

  
 
